package logics;

public class swapping_number {

	public static void main(String[] args) {
		
		System.out.println("before swapping");
		int x=10;
		int y=11;
		int z;
		System.out.println("x:-"+x);
		System.out.println("y:-"+y);

		
		System.out.println("after swapping");
		z=x;
		x=y;
		y=z;
		System.out.println("x:-"+x);
        System.out.println("y:-"+y);
	}

}
