package logics;

public class Star_pattern3 {

	public static void main(String[] args) {
		
		
	    for (int i=1;i<=3;i++) {         //row/outer
		    	
	       for(int j=1;j<=4;j++) {         //inner
	    		  
	    	 System.out.print("*");
	    		 
	    	  }
	       
	    	  System.out.println();
	    }	
		

	}

}
