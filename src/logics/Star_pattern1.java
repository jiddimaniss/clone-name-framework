
package logics;

public class Star_pattern1 {

	public static void main(String[] args) {
		
		
		
		 for (int i=0;i<=4;i++) {  //outer loop
	    	  
	    	   for(int j=1;j<=i;j++) {  //inner loop
	    		   
	    		   System.out.print(" ");
	    	    }
	    	   
	    	  System.out.println("*");
	    	  
	      }
		
		

	}

}
